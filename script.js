// Dark mode
function darkMode() {
    document.body.style.backgroundColor = 'black'
    const first = document.getElementById('color')
    first.style.color = 'white'
    
}



// Light mode
function lightMode() {
    document.body.style.backgroundColor = 'white'
    const second = document.getElementById('color')
    second.style.color = 'black'
}

// purple
function purpleColor () {
    document.body.style.backgroundColor = 'purple'
    const third = document.getElementById('color')
    third.style.color = 'red'
}

// yellow 
function yellowColor() {
    document.body.style.backgroundColor = 'yellow'
    const fourth = document.getElementById('color')
    fourth.style.color = 'blue'
}